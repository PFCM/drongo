// binary gen generates unrolled versions of simple elementwise arithmetic.
package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/format"
	"log"
	"os"
	"text/template"
)

var (
	unrollsFlag     = flag.Int("unrolls", 16, "`number` of loop iterations to unroll")
	packageNameFlag = flag.String("package-name", "gen", "go package `name` for the generated code")
	outputFlag      = flag.String("output", "", "`path` to write output to, writes to stdout if empty")
)

var scalarBinaryOps = []binaryOp{
	// elementwise float32
	{
		Name:   "ScalarAddFloat32",
		Type:   "float32",
		Stride: 1,
		Expr: func(i string) string {
			return fmt.Sprintf("a[%s] + b[%s]", i, i)
		},
	},
	// TODO: needs stride to actually work, and probably something better for multi-line output?
	// {
	// 	Name: "ScalarAddSubFloat32",
	// 	Type: "float32",
	// 	Stride: 2,
	// 	Expr: func(i string) string {
	// 		return fmt.Sprintf("a[%s] - b[%s]\n\ta[%s] + b[%s]", i, i, i+1, i+1)
	// 	},
	// },
	{
		Name:   "DivFloat32",
		Type:   "float32",
		Stride: 1,
		Expr: func(i string) string {
			return fmt.Sprintf("a[%s] / b[%s]", i, i)
		},
	}, {
		Name:   "MaxFloat32",
		Type:   "float32",
		Stride: 1,
		Expr: func(i string) string {
			return fmt.Sprintf("max(a[%s], b[%s])", i, i)
		},
	}, {
		Name:   "MinFloat32",
		Type:   "float32",
		Stride: 1,
		Expr: func(i string) string {
			return fmt.Sprintf("min(a[%s], b[%s])", i, i)
		},
	}, {
		Name:   "ScalarMulFloat32",
		Type:   "float32",
		Stride: 1,
		Expr: func(i string) string {
			return fmt.Sprintf("a[%s] * b[%s]", i, i)
		},
	}, {
		Name:   "ScalarSubFloat32",
		Type:   "float32",
		Stride: 1,
		Expr: func(i string) string {
			return fmt.Sprintf("a[%s] - b[%s]", i, i)
		},
	},
}

func main() {
	flag.Parse()

	out := os.Stdout
	if *outputFlag != "" {
		f, err := os.Create(*outputFlag)
		if err != nil {
			log.Fatal(err)
		}
		out = f
	}

	var b bytes.Buffer
	if err := binaryTmpl.Execute(&b, struct {
		PackageName string
		Unrolls     int
		Ops         []binaryOp
	}{
		PackageName: *packageNameFlag,
		Unrolls:     *unrollsFlag,
		Ops:         scalarBinaryOps,
	}); err != nil {
		log.Fatal(err)
	}

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		log.Fatal(err)
	}
	if _, err := out.Write(formatted); err != nil {
		log.Fatal(err)
	}
}

type binaryOp struct {
	Name   string
	Type   string // go type
	Expr   func(string) string
	Stride int
}

var binaryTmpl = template.Must(template.New("binary_ops").Funcs(template.FuncMap{
	"mul": func(a, b int) int { return a * b },
}).Parse(`
package {{ .PackageName }}

// Code generated by cmd/gen DO NOT EDIT.

import "fmt"

{{ range .Ops -}}
{{ $op := . }}
func unrolled{{.Name}}(a, b, c []{{.Type}}) {
	if len(a) != len(b) || len(a) != len(c) {
		panic(fmt.Errorf("incompatible lengths: %d, %d, %d", len(a), len(b), len(c)))
	}
	for len(a) > {{$.Unrolls}} {
		{{ range $i := $.Unrolls -}}
		c[{{$i}}] = {{print $i | call $op.Expr}}
		{{ end }}
		a = a[{{ mul $.Unrolls .Stride }}:]
		b = b[{{ mul $.Unrolls .Stride }}:]
		c = c[{{ mul $.Unrolls .Stride }}:]
	}
	for i := range c {
		c[i] = {{call .Expr "i"}}
	}
}
{{ end -}}
`))
