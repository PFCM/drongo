// binary gen generates unrolled versions of simple elementwise arithmetic.
package main

import (
	"bytes"
	"fmt"
	"go/format"
	"log"
	"os"
	"text/template"
)

func main() {
	var b bytes.Buffer
	if err := binaryTmpl.Execute(&b, binaryOp{
		PackageName: "drongo",
		OpName:      "MulFloat32",
		Type:        "float32",
		Expr: func(i string) string {
			return fmt.Sprintf("a[%s] * b[%s]", i, i)
		},
	}); err != nil {
		log.Fatal(err)
	}

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		log.Fatal(err)
	}
	if _, err := os.Stdout.Write(formatted); err != nil {
		log.Fatal(err)
	}
}

type binaryOp struct {
	PackageName string
	OpName      string
	Type        string // go type
	Expr        func(string) string
}

var binaryTmpl = template.Must(template.New("binary_ops").Parse(`
package {{ .PackageName }}

// Code generated by cmd/gen DO NOT EDIT.

import "fmt"

func unrolled8Scalar{{.OpName}}(a, b, c []{{.Type}}) {
	if len(a) > len(b) || len(a) > len(c) {
		panic(fmt.Errorf("incompatible lengths: %d, %d, %d", len(a), len(b), len(c)))
	}
	for len(a) > 8 {
		{{ range $i := 8 -}}
		c[{{$i}}] = {{print $i | call $.Expr}}
		{{ end }}
		a = a[8:]
		b = b[8:]
		c = c[8:]
	}
	for i := range c {
		c[i] = {{call .Expr "i"}}
	}
}
`))
